Map::Map() {
	dirt = TextureManager::LoadTexture("resources/tiles/dirt.png");
	grass = TextureManager::LoadTexture("resources/tiles/grass.png");
	water = TextureManager::LoadTexture("resources/tiles/water.png");
	LoadMap(levelOne);

	source.x = source.y = 0;
	source.w = destination.w = 64;
	source.h = destination.h = 64;
	
	destination.x = destination.y = 0;
}

Map::~Map() {

}

void Map::LoadMap(int array[20][25]) {
	for (int row = 0; row < 20; row++) {
		for (int column = 0; column < 25; column++) {
			map[row][column] = array[row][column]; 
		}
	}
}

void Map::DrawMap() {
	int type = 0;
	for (int row = 0; row < 20; row++) {
		for (int column = 0; column < 25; column++) {

			type = map[row][column];

			destination.x = column * 64;
			destination.y = row * 64;

			switch (type) {
			case 0:
				TextureManager::Draw(water, source ,destination);
				break;
			case 1:
				TextureManager::Draw(grass, source, destination);

				break;
			case 2:
				TextureManager::Draw(dirt, source, destination);
				break;
			}
		}
	}
}